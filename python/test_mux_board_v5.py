import pyvisa
rm = pyvisa.ResourceManager('@py') # remove '@py' if running on Windows
print(rm.list_resources())
samd21 = rm.open_resource('USB0::51966::16384::123456::0::INSTR')
print(samd21.query("*IDN?"))
print(samd21.query("MUX1:SEL?"))
samd21.write("MUX1:SEL 16")
print(samd21.query("MUX1:SEL?"))
print(samd21.query("MUX1:EN?"))
samd21.write("MUX1:EN 1")
print(samd21.query("MUX1:EN?"))
print(samd21.query("MUX2:SEL?"))
samd21.write("MUX2:SEL 15")
print(samd21.query("MUX2:SEL?"))
print(samd21.query("MUX2:EN?"))
samd21.write("MUX2:EN 1")
print(samd21.query("MUX2:EN?"))
samd21.write("*RST")
print(samd21.query("MUX1:SEL?"))
print(samd21.query("MUX1:EN?"))
print(samd21.query("MUX2:SEL?"))
print(samd21.query("MUX2:EN?"))
